                            <p>Number: HMS. 46/VII/BPSDMP-2020</p>

                            <p>Jakarta (4/7) - In order to give insight on the organisation of transportation in the
                                European Union to cadets of colleges managed by the Ministry of Transportation, the
                                Agency for Development of Human Resources on Transportation (BPSDMP), in collaboration
                                with the European Union Delegation to Indonesia and Brunei Darussalam, held a virtual
                                public lecture featuring Head of International Relations Unit of DG MOVE, European
                                Commission, Mr. Harvey Rouse, as main speaker and Head of the Center for the Development
                                of Sea Transportation Human Resources, Capt. Sahattua Simatupang as moderator. </p>

                            <p>Entitled "Current Transportation Condition in the European Union and Its Challenges", the
                                event was attended by 955 participants comprised of cadets, lecturers, and officials. It
                                started off with remarks by the Secretary of BPSDMP, Mr M. Popik Montanasyah and Head of
                                Economic and Trade Section of the European Union Delegation to Indonesia and Brunei
                                Darussalam, Mr Raffaele Quarto, as joint host.
                            </p>

                            <p>
                                In his remarks, Popik expressed his appreciation to the European Commission and EU
                                Delegation to Indonesia and Brunei Darussalam, for their cooperation and willingness in
                                organising the public lecture.
                            </p>

                            <p>
                                "As we all know, European countries possess advanced technology and transportation
                                management system. Hence, through this public lecture, we hope that our speakers will be
                                able to widen the horizon of our knowledge regarding technology, transportation
                                management system, latest transportation condition in the European Union, through
                                information and knowledge sharing," said Popik.
                            </p>

                            <p>
                                Further, Popik introduced the BPSDMP, saying that BPSDMP manages 26 schools, education
                                and training institutions across Indonesia, with a total enrolment of around 16,000
                                cadets. He also explained BPSDMP's strategy in improving the quality and competence of
                                its graduates to be able to compete in national and international transportation
                                industry. One way to achieve that is to collaborate with both domestic and foreign
                                stakeholders.
                            </p>

                            <p>
                                "In accordance with instruction of the Minister of Transportation and Head of BPSDMP,
                                and to realize one of the priorities of President Joko Widodo's administration, i.e. to
                                develop human resource capability, BPSDMP focuses to equip graduates with soft skills
                                such as foreign language skills, discipline and strong character, so as to meet national
                                and international standards ", Popik stated.
                            </p>
                            <p>This type of activity according to Popik, sharpens cadets’ foreign language skills of
                                cadets in absorbing information and fosters their self-confidence by actively
                                participating in the discussions.</p>
                            <p>
                                "I convey the message of the Head of BPSDMP for cadets to take this public lecture
                                seriously. This is a good opportunity, so you must maximise this forum to exchange
                                knowledge and experience about update on transportation condition," Popik underlined.

                            </p>

                            <p>
                                Head of Economic and Trade Section of the EU Delegation to Indonesia and Brunei
                                Darussalam, Mr. Raffaele Quarto, in his speech said that development of human resources
                                is important for every country. He hopes that the public lecture would be a good
                                opportunity for all parties to learn about current transportation condition.
                            </p>

                            <p>
                                "Human resource development is a priority for almost all countries in the world. This
                                public lecture hopefully can become one of the opportunities to develop insight by
                                exchanging knowledge regarding current transportation condition," Quarto said.
                            </p>

                            <p>
                                Alongside Popik, were present also the Head of Land Transportation Human Resource
                                Development Centre, Soeharto, and Head of Air Transportation Human Resource Development
                                Centre, Heri Sudarmadji. Both also expressed their appreciation and actively took part
                                in discussion on transportation system management in the EU during the Covid-19 pandemic
                                era to ensure smooth distribution of logistics.
                            </p>



                            
